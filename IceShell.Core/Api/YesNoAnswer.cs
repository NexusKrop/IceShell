namespace IceShell.Core.Api;

/// <summary>
/// An enumeration representing the answers to Yes/No questions.
/// </summary>
public enum YesNoAnswer
{
    /// <summary>
    /// The answer is neither Yes nor No.
    /// </summary>
    Invalid,
    /// <summary>
    /// An affirmative answer.
    /// </summary>
    Yes,
    /// <summary>
    /// A negative answer.
    /// </summary>
    No
}
